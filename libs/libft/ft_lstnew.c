#include "libft.h"

t_list	*ft_lstnew(char *content)
{
	t_list	*ret;

	ret = (t_list *)malloc(sizeof(t_list));
	if (!ret)
		return (0);
	ret->content = content;
	ret->next = NULL;
	ret->label = 0;
	return (ret);
}
